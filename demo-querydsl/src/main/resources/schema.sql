CREATE TABLE base (
  id   BIGINT generated BY DEFAULT AS IDENTITY,
  name VARCHAR(255),
  PRIMARY KEY (id)
);
CREATE TABLE pizza (
  id      BIGINT generated BY DEFAULT AS IDENTITY,
  name    VARCHAR(255),
  price   DECIMAL(19, 2),
  base_id BIGINT,
  PRIMARY KEY (id)
);
CREATE TABLE pizza_toppings (
  pizza_id    BIGINT NOT NULL,
  toppings_id BIGINT NOT NULL,
  PRIMARY KEY (pizza_id, toppings_id)
);
CREATE TABLE topping (
  id   BIGINT generated BY DEFAULT AS IDENTITY,
